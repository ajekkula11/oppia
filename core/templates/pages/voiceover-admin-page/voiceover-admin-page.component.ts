/**
 * @fileoverview Voiceover admin component.
 */

import {
  ChangeDetectionStrategy,
  Component,
  OnInit,
  ChangeDetectorRef,
} from '@angular/core';
import {NgbModal, NgbModalRef} from '@ng-bootstrap/ng-bootstrap';
import {
  VoiceoverBackendApiService,
  LanguageAccentToDescription,
  LanguageCodesMapping,
  LanguageAccentMasterList,
  LanguageAccentCodesToSupportsAutogeneration,
} from 'domain/voiceover/voiceover-backend-api.service';
import {VoiceoverRemovalConfirmModalComponent} from './modals/language-accent-removal-confirm-modal.component';
import {NativeDateAdapter} from '@angular/material/core';
import {FormControl, FormGroup} from '@angular/forms';
import {CloudTaskRun} from 'domain/cloud-task/cloud-task-run.model';
import {AutogeneratedVoiceoverRunInfoModalComponent} from './modals/autogenerated-voiceover-run-info-modal.component';
import {EditVoiceoverRegenerationSupportModalComponent} from './modals/edit-voiceover-regeneration-support-modal.component';
import {LanguageUtilService} from 'domain/utilities/language-util.service';
import {AlertsService} from 'services/alerts.service';

interface LanguageAccentCodeToLanguageCode {
  [languageAccentCode: string]: string;
}

export interface LanguageAccentDescriptionToCode {
  [languageAccentDescription: string]: string;
}

@Component({
  selector: 'oppia-voiceover-admin-page',
  templateUrl: './voiceover-admin-page.component.html',
  providers: [NativeDateAdapter],
  changeDetection: ChangeDetectionStrategy.OnPush,
})
export class VoiceoverAdminPageComponent implements OnInit {
  // These properties are initialized using Angular lifecycle hooks
  // and we need to do non-null assertion. For more information, see
  // https://github.com/oppia/oppia/wiki/Guide-on-defining-types#ts-7-1
  constructor(
    private alertsService: AlertsService,
    private ngbModal: NgbModal,
    private voiceoverBackendApiService: VoiceoverBackendApiService,
    private cdr: ChangeDetectorRef,
    private languageUtilService: LanguageUtilService
  ) {}

  languageAccentCodeToLanguageCode!: LanguageAccentCodeToLanguageCode;
  supportedLanguageAccentCodesToDescriptions!: LanguageAccentToDescription;
  languageAccentCodesToSupportsAutogeneration!: LanguageAccentCodesToSupportsAutogeneration;
  availableLanguageAccentDescriptionsToCodes!: LanguageAccentDescriptionToCode;
  languageAccentCodesToDescriptionsMasterList!: LanguageAccentToDescription;
  languageCodesMapping!: LanguageCodesMapping;
  pageIsInitialized: boolean = false;
  languageAccentDropdownIsShown: boolean = false;
  languageAccentCodeIsPresent: boolean = false;
  languageAccentMasterList!: LanguageAccentMasterList;
  cloudSupportedLanguageAccentCodes: string[] = [];
  columnsToDisplay: string[] = [
    'voiceArtist',
    'languageCode',
    'languageAccentCode',
    'languageAccentCodeModify',
  ];

  autogeneratedVoiceoverRecordsDisplayedColumns: string[] = [
    'state',
    'functionID',
    'numberOfRetries',
    'startedOn',
    'lastUpdated',
    'details',
  ];

  cloudTaskRunList: CloudTaskRun[] = [];
  cloudTaskRunIdToIndex: {[cloudTaskRunId: string]: number} = {};

  voiceArtistsDataCount: number = 0;
  fetchingRegeneratedVoiceoverData: boolean = false;
  regenerationRecordsErrorMessage: string | null = null;

  range = new FormGroup({
    start: new FormControl(null),
    end: new FormControl(null),
  });

  explorationIDForVoiceoverRegeneration: string = '';
  explorationDataResponseForVoiceoverRegeneration: string | null = null;
  explorationTitleForVoiceoverRegeneration: string | null = null;
  explorationDataForVoiceoverRegenerationIsLoading: boolean = false;
  autogeneratableLanguageAccentCodes: string[] = [];
  isExplorationDataResponseContainerShown: boolean = false;
  selectedLanguageAccentForExplorationVoiceoverRegeneration: string | null =
    null;
  toastMessage: string = '';

  languageAccentControl = new FormControl(null);

  ngOnInit(): void {
    this.voiceoverBackendApiService
      .fetchVoiceoverAdminDataAsync()
      .then(response => {
        this.languageCodesMapping = response.languageCodesMapping;
        this.languageAccentCodeToLanguageCode = {};
        this.supportedLanguageAccentCodesToDescriptions = {};
        this.languageAccentCodesToDescriptionsMasterList = {};
        this.availableLanguageAccentDescriptionsToCodes = {};
        this.languageAccentCodesToSupportsAutogeneration = {};
        this.initializeLanguageAccentCodesFields(
          response.languageAccentMasterList
        );
        this.languageAccentMasterList = response.languageAccentMasterList;
        this.cloudSupportedLanguageAccentCodes =
          response.autoGeneratableLanguageAccentCodes;
        this.pageIsInitialized = true;
        this.cdr.detectChanges();
      });
  }

  initializeLanguageAccentCodesFields(
    languageAccentMasterList: LanguageAccentMasterList
  ): void {
    for (let languageCode in languageAccentMasterList) {
      const languageAccentCodesToDescriptions =
        languageAccentMasterList[languageCode];

      for (let languageAccentCode in languageAccentCodesToDescriptions) {
        const languageAccentDescription =
          languageAccentCodesToDescriptions[languageAccentCode];

        this.languageAccentCodeToLanguageCode[languageAccentCode] =
          languageCode;

        this.languageAccentCodesToDescriptionsMasterList[languageAccentCode] =
          languageAccentDescription;
      }
    }

    for (let languageCode in this.languageCodesMapping) {
      const languageAccentToSupportsAutogeneration =
        this.languageCodesMapping[languageCode];

      for (let languageAccentCode in languageAccentToSupportsAutogeneration) {
        const languageAccentDescription =
          this.languageAccentCodesToDescriptionsMasterList[languageAccentCode];

        this.supportedLanguageAccentCodesToDescriptions[languageAccentCode] =
          languageAccentDescription;

        this.languageAccentCodesToSupportsAutogeneration[languageAccentCode] =
          languageAccentToSupportsAutogeneration[languageAccentCode];
      }
    }

    for (let languageAccentCode in this
      .languageAccentCodesToDescriptionsMasterList) {
      const languageAccentDescription =
        this.languageAccentCodesToDescriptionsMasterList[languageAccentCode];

      if (
        !(languageAccentCode in this.supportedLanguageAccentCodesToDescriptions)
      ) {
        this.availableLanguageAccentDescriptionsToCodes[
          languageAccentDescription
        ] = languageAccentCode;
      }
    }

    this.languageAccentCodeIsPresent =
      Object.keys(this.supportedLanguageAccentCodesToDescriptions).length !== 0;
  }

  addLanguageAccentCodeSupport(languageAccentCodeToAdd: string): void {
    const languageCode =
      this.languageAccentCodeToLanguageCode[languageAccentCodeToAdd];
    const languageAccentDescription =
      this.languageAccentCodesToDescriptionsMasterList[languageAccentCodeToAdd];

    this.supportedLanguageAccentCodesToDescriptions[languageAccentCodeToAdd] =
      languageAccentDescription;
    delete this.availableLanguageAccentDescriptionsToCodes[
      languageAccentDescription
    ];

    if (!(languageCode in this.languageCodesMapping)) {
      this.languageCodesMapping[languageCode] = {};
    }
    this.languageCodesMapping[languageCode][languageAccentCodeToAdd] = false;
    this.languageAccentCodesToSupportsAutogeneration[languageAccentCodeToAdd] =
      false;

    this.languageAccentCodeIsPresent =
      Object.keys(this.supportedLanguageAccentCodesToDescriptions).length !== 0;
    this.removeLanguageAccentDropdown();
    this.saveUpdatedLanguageAccentSupport();
  }

  isAutogenerationSupportedByCloudService(languageAccentCode: string): boolean {
    return this.cloudSupportedLanguageAccentCodes?.includes(languageAccentCode);
  }

  updateSupportsAutogenerationField(
    languageAccentCode: string,
    supportsAutogeneration: boolean
  ): void {
    const languageCode =
      this.languageAccentCodeToLanguageCode[languageAccentCode];
    const languageDescription =
      this.languageUtilService.getAudioLanguageDescription(languageCode);
    const languageAccentCodeDescription =
      this.supportedLanguageAccentCodesToDescriptions[languageAccentCode];

    let modalRef: NgbModalRef = this.ngbModal.open(
      EditVoiceoverRegenerationSupportModalComponent,
      {backdrop: 'static'}
    );

    modalRef.componentInstance.languageDescription = languageDescription;
    modalRef.componentInstance.languageCode = languageCode;
    modalRef.componentInstance.languageAccentDescription =
      languageAccentCodeDescription;
    modalRef.componentInstance.supportsAutogeneration = supportsAutogeneration;

    modalRef.result.then(
      () => {
        this.languageCodesMapping[languageCode][languageAccentCode] =
          supportsAutogeneration;
        this.saveUpdatedLanguageAccentSupport();
      },
      () => {
        this.languageAccentCodesToSupportsAutogeneration[languageAccentCode] =
          !supportsAutogeneration;
        this.cdr.detectChanges();
      }
    );
  }

  removeLanguageAccentCodeSupport(languageAccentCodeToRemove: string): void {
    const languageCode =
      this.languageAccentCodeToLanguageCode[languageAccentCodeToRemove];
    const languageAccentDescription =
      this.languageAccentCodesToDescriptionsMasterList[
        languageAccentCodeToRemove
      ];

    let modalRef: NgbModalRef = this.ngbModal.open(
      VoiceoverRemovalConfirmModalComponent,
      {
        backdrop: 'static',
      }
    );

    modalRef.componentInstance.languageAccentDescription =
      languageAccentDescription;

    modalRef.result.then(
      () => {
        delete this.supportedLanguageAccentCodesToDescriptions[
          languageAccentCodeToRemove
        ];
        this.availableLanguageAccentDescriptionsToCodes[
          languageAccentDescription
        ] = languageAccentCodeToRemove;

        delete this.languageCodesMapping[languageCode][
          languageAccentCodeToRemove
        ];

        if (Object.keys(this.languageCodesMapping[languageCode]).length === 0) {
          delete this.languageCodesMapping[languageCode];
        }

        this.languageAccentCodeIsPresent =
          Object.keys(this.supportedLanguageAccentCodesToDescriptions)
            .length !== 0;
        this.saveUpdatedLanguageAccentSupport();
        this.cdr.detectChanges();
      },
      () => {
        // Note to developers:
        // This callback is triggered when the Cancel button is
        // clicked. No further action is needed.
      }
    );
  }

  saveUpdatedLanguageAccentSupport(): void {
    this.voiceoverBackendApiService
      .updateVoiceoverLanguageCodesMappingAsync(this.languageCodesMapping)
      .then(() => {
        this.removeLanguageAccentDropdown();
      });
  }

  showLanguageAccentDropdown(): void {
    this.languageAccentDropdownIsShown = true;
  }

  removeLanguageAccentDropdown(): void {
    this.languageAccentDropdownIsShown = false;
  }

  fetchVoiceoverRegenerationRecord(): void {
    this.fetchingRegeneratedVoiceoverData =
      !this.fetchingRegeneratedVoiceoverData;

    let startDate = this.range.value.start;
    let startDateText = startDate ? startDate.toISOString() : '';

    let endDate = this.range.value.end;
    let endDateText = endDate ? endDate.toISOString() : '';

    this.voiceoverBackendApiService
      .fetchVoiceoverRegenerationRecordAsync(startDateText, endDateText)
      .then(
        response => {
          this.cloudTaskRunList = response;
          this.fetchingRegeneratedVoiceoverData = false;

          for (let i = 0; i < this.cloudTaskRunList.length; i++) {
            this.cloudTaskRunIdToIndex[this.cloudTaskRunList[i].id] = i;
          }

          this.cdr.detectChanges();
        },
        () => {
          this.fetchingRegeneratedVoiceoverData = false;
        }
      );
  }

  openCloudTaskRunDetailModal(cloudTaskRunId: string): void {
    const cloudTaskRun =
      this.cloudTaskRunList[this.cloudTaskRunIdToIndex[cloudTaskRunId]];
    let modalRef: NgbModalRef = this.ngbModal.open(
      AutogeneratedVoiceoverRunInfoModalComponent,
      {
        backdrop: 'static',
        size: 'xl',
        centered: true,
      }
    );
    modalRef.componentInstance.cloudTaskRun = cloudTaskRun;
  }

  closeExpDataResponseContainer(): void {
    this.isExplorationDataResponseContainerShown = false;
    this.explorationIDForVoiceoverRegeneration = '';
    this.explorationDataResponseForVoiceoverRegeneration = null;
    this.explorationTitleForVoiceoverRegeneration = null;
  }

  fetchExplorationDataForVoiceoverRegeneration(): void {
    this.explorationDataForVoiceoverRegenerationIsLoading = true;
    this.voiceoverBackendApiService
      .fetchExplorationDataForVoiceoverAsync(
        this.explorationIDForVoiceoverRegeneration as string
      )
      .then(response => {
        this.explorationDataResponseForVoiceoverRegeneration =
          response.responseMessage;
        this.explorationDataForVoiceoverRegenerationIsLoading = false;

        this.explorationTitleForVoiceoverRegeneration = response.explorationData
          ?.explorationTitle as string;
        this.isExplorationDataResponseContainerShown = true;
        this.autogeneratableLanguageAccentCodes =
          response.explorationData?.autogeneratableLanguageAccentCodes || [];
        this.cdr.detectChanges();
      });
  }

  updateLanguageAccentForVoiceoverRegenerationChoice(
    languageAccentCode: string
  ): void {
    this.selectedLanguageAccentForExplorationVoiceoverRegeneration =
      languageAccentCode;
  }

  generateVoiceoversForExploration(): void {
    this.showToast(
      'Voiceovers will be regenerated automatically in the ' +
        'background, please reload the page after a few minutes to ' +
        'see the changes.',
      10000
    );
    this.voiceoverBackendApiService.regenerateVoiceoversForExplorationAsync(
      this.explorationIDForVoiceoverRegeneration as string,
      this.selectedLanguageAccentForExplorationVoiceoverRegeneration as string
    );
    this.selectedLanguageAccentForExplorationVoiceoverRegeneration = null;
    this.languageAccentControl.reset();
    this.cdr.detectChanges();
  }

  showToast(message: string, timeout = 10000): void {
    this.toastMessage = message;
    this.cdr.detectChanges();

    setTimeout(() => {
      const toastEl = document.querySelector(
        '.information-toast'
      ) as HTMLElement;
      if (toastEl) {
        toastEl.classList.add('fade-out');

        setTimeout(() => {
          this.toastMessage = '';
          this.cdr.detectChanges();
        }, 800);
      }
    }, timeout);
  }

  getFunctionIdText(functionId: string): string {
    if (functionId === 'regenerate_voiceovers_on_exploration_update') {
      return 'Exploration content updated';
    } else if (
      functionId === 'regenerate_voiceovers_on_exploration_added_to_topic'
    ) {
      return 'Exploration added to topic';
    } else if (
      functionId ===
      'regenerate_voiceovers_of_exploration_for_given_language_accent'
    ) {
      return 'Voiceover regeneration for the exploration in the chosen language accent';
    }
    return '';
  }
}
