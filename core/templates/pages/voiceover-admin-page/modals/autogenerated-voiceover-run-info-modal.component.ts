/**
 * @fileoverview Voiceover autogeneration information modal component.
 */

import {Component} from '@angular/core';
import {NgbActiveModal} from '@ng-bootstrap/ng-bootstrap';
import {ConfirmOrCancelModal} from 'components/common-layout-directives/common-elements/confirm-or-cancel-modal.component';
import {CloudTaskRun} from 'domain/cloud-task/cloud-task-run.model';

@Component({
  selector: 'oppia-autogenerated-voiceover-run-info-modal',
  templateUrl: './autogenerated-voiceover-run-info-modal.component.html',
})
export class AutogeneratedVoiceoverRunInfoModalComponent extends ConfirmOrCancelModal {
  cloudTaskRun!: CloudTaskRun;
  constructor(private ngbActiveModal: NgbActiveModal) {
    super(ngbActiveModal);
  }

  close(): void {
    this.ngbActiveModal.close();
  }

  copyErrorToClipboard(error: string): void {
    navigator.clipboard.writeText(error);
  }

  getFunctionIdText(functionId: string): string {
    if (functionId === 'regenerate_voiceovers_on_exploration_update') {
      return 'regenerate_voiceovers_on_exploration_update \n ("Exploration content updated")';
    } else if (
      functionId === 'regenerate_voiceovers_on_exploration_added_to_topic'
    ) {
      return 'regenerate_voiceovers_on_exploration_added_to_topic \n ("Exploration added to topic")';
    } else if (
      functionId ===
      'regenerate_voiceovers_of_exploration_for_given_language_accent'
    ) {
      return 'regenerate_voiceovers_of_exploration_for_given_language_accent \n ("Voiceover regeneration for the exploration in the chosen language accent")';
    }
    return '';
  }
}
